  k2=l2*sind(theta2);
  y=atand(k2/k1);
  theta1=atand(y2/x2)-y;
  b1=l1*cosd(theta1);
  a1=l1*sind(theta1);
  b2=b1+l2*cosd(theta1+theta2);
  a2=a1+l2*sind(theta1+theta2);
  plot([0,b1],[0,a1],'g');
  hold on;
  plot([b1,b2],[a1,a2],'r');
